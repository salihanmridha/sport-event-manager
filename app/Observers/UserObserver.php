    /**
     * Handle the User "created" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function created(User $user)
    {
        if ($user->roles->isEmpty()) {
            $userRole = Role::firstOrCreate([
                'title' => 'User'
            ]);
            $user->roles()->sync($userRole->id);
        }
    }

